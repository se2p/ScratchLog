package fim.unipassau.de.scratchLog.util;

import fim.unipassau.de.scratchLog.util.validation.PasswordValidator;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertAll;
import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNull;

public class CustomPasswordGeneratorTest {

    private static final int NUM_CHARACTERS = 10;

    @Test
    public void testGeneratePassword() {
        String password = CustomPasswordGenerator.generatePassword(NUM_CHARACTERS);
        assertAll(
                () -> assertEquals(NUM_CHARACTERS, password.length()),
                () -> assertNull(PasswordValidator.validate(password, password))
        );
    }

}
